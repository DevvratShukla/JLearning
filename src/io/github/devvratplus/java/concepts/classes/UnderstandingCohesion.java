package io.github.devvratplus.java.concepts.classes;

public class UnderstandingCohesion {

  /*
   * Cohesion is used to indicate the degree to which a class has a single, well-focused purpose.
   * Coupling is all about how classes interact with each other, on the other hand cohesion focuses
   * on how single class is designed. Higher the cohesiveness of the class, better is the OO design.
   * 
   * Benefits of Higher Cohesion:
   * 
   * Highly cohesive classes are much easier to maintain and less frequently changed. Such classes
   * are more usable than others as they are designed with a well-focused purpose.
   * 
   * The Single Responsibility principle (SRP) states that: There should never be more than one
   * reason for a class to change.
   */
}
